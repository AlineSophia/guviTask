package com.guvi;

public class PrintNumbers {
	// Main method
	public static void main(String[] args) {
		// Initialize the variable
		int startNumber = 10;
		int endNumber = 50;
		// for statement
		for (int i = startNumber; i <= endNumber; i++) {
			// Printing the value
			System.out.println(i);
		}

	}

}
